export const getBlogById = (id) => {
  return fallbackBlogs.find(blog => blog._id === id);
};

export const getRelatedBlogs = (blogId, limit = 3) => {
  const currentBlog = getBlogById(blogId);
  if (!currentBlog) return [];
  
  // Get blogs with matching tags
  return fallbackBlogs
    .filter(blog => 
      blog._id !== blogId && 
      blog.tags.some(tag => currentBlog.tags.includes(tag))
    )
    .slice(0, limit);
};

export const getPaginatedBlogs = (page = 1, limit = 6) => {
  const startIndex = (page - 1) * limit;
  const endIndex = startIndex + limit;
  const paginatedBlogs = fallbackBlogs.slice(startIndex, endIndex);
  
  return {
    blogs: paginatedBlogs,
    totalBlogs: fallbackBlogs.length,
    totalPages: Math.ceil(fallbackBlogs.length / limit),
    currentPage: page
  };
};
